public class InfiniteForLoop { 
	public static void main(String[] args) { 
	for (int i = 0; i < 10; i--) { 
		System.out.println(i); 
		}	 
	} 
}

/*
1.It will run infinite loop because the condition is i<10 in which we have to do the i++ but here has given that we i-- so it will run infinitely till store block not becomes full.
2.Loop Control - i<10 then i should be ++ means i++.
*/